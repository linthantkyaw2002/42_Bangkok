#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	n;
	int		i;
	char	nums[10];

	n = nb;
	if (n == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	i = 0;
	while (n > 0)
	{
		nums[i++] = (n % 10) + '0';
		n = n / 10;
	}
	while (--i >= 0)
	{
		write(1, &nums[i], 1);
	}
}
